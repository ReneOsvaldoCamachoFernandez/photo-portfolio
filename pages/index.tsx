import { Tab } from "@headlessui/react";
import Head from "next/head";
import Link from "next/link";
import Masonry from "react-masonry-css";
import classNames from "classnames";
import Image from "next/image";
import bgImage from "../public/photography-bg.jpg";

////////////////////////
//LIGTHGALLERY
////////////////////////
import type { LightGallery } from "lightgallery/lightgallery";
import LightGalleryComponent from "lightgallery/react";

// import styles
import "lightgallery/css/lightgallery.css";
import "lightgallery/css/lg-zoom.css";
import "lightgallery/css/lg-thumbnail.css";

// import plugins if you need
import lgThumbnail from "lightgallery/plugins/thumbnail";
import lgZoom from "lightgallery/plugins/zoom";
//////////////////////

import ocean1 from "public/photos/oceans/ocean-1.jpg";
import ocean2 from "public/photos/oceans/ocean-2.jpg";
import ocean3 from "public/photos/oceans/ocean-3.jpg";
import ocean4 from "public/photos/oceans/ocean-4.jpg";
import ocean5 from "public/photos/oceans/ocean-5.jpg";
import { useRef } from "react";

const tabs = [
  {
    key: "all",
    display: "All",
  },
  {
    key: "oceans",
    display: "Oceans",
  },
  {
    key: "forest",
    display: "Forest",
  },
];
const images = [ocean1, ocean2, ocean3, ocean4, ocean5];

export default function Home() {
  const ligthboxRef = useRef<LightGallery | null>(null);

  return (
    <div className="h-full overflow-auto">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Image
        className="fixed top-o left-0 z-0"
        src={bgImage}
        alt="bg placeholder"
        placeholder="blur"
        priority={true}
      />
      <div className="fixed top-0 left-0 w-full h-full from-stone-900 bg-gradient-to-t z-10"></div>
      <header className="bg-black fixed w-full top-0 z-30 flex justify-between items-center h-[90px] px-10">
        <div>
          <span className="uppercase text-lg font-medium">
            Photography Portfolio
          </span>
        </div>

        <Link
          href="#"
          className="rounded-3xl bg-white text-stone-700 px-3 py-2 hover:bg-opacity-90"
        >
          Get in touch
        </Link>
      </header>
      <main className="pt-[110px] z-20 relative">
        <div className="flex flex-col items-center h-full">
          <Tab.Group>
            <Tab.List className="flex  items-center gap-20">
              {tabs.map((tab) => (
                <Tab key={tab.key} className="p-2">
                  {({ selected }) => (
                    <span
                      className={classNames(
                        "uppercase text-lg",
                        selected ? "text-white" : "text-stone-600"
                      )}
                    >
                      {tab.display}
                    </span>
                  )}
                </Tab>
              ))}
            </Tab.List>
            <Tab.Panels className="p-2 sm:p-4 h-full max-w-[900px] w-full my-6">
              <Tab.Panel className="overflow-auto">
                <Masonry
                  breakpointCols={2}
                  className="flex gap-4"
                  columnClassName=""
                >
                  {images.map((image, indx) => (
                    <Image
                      key={image.src}
                      src={image}
                      className="my-4 hover:opacity-70 cursor-pointer"
                      alt="placeholder"
                      placeholder="blur"
                      onClick={() => {
                        ligthboxRef.current?.openGallery(indx);
                      }}
                    />
                  ))}
                </Masonry>
                <LightGalleryComponent
                  onInit={(ref) => {
                    if (ref) ligthboxRef.current = ref.instance;
                  }}
                  speed={500}
                  plugins={[lgThumbnail, lgZoom]}
                  dynamic={true}
                  dynamicEl={images.map((image) => ({
                    src: image.src,
                    thumb: image.src,
                  }))}
                ></LightGalleryComponent>
              </Tab.Panel>
              <Tab.Panel>Oceans</Tab.Panel>
              <Tab.Panel>Forest</Tab.Panel>
            </Tab.Panels>
          </Tab.Group>
        </div>
      </main>
      <footer className="uppercase text-lg font-medium flex justify-center items-center h-[90px] z-20 relative">
        <p>placeholder for footer</p>
      </footer>
    </div>
  );
}
